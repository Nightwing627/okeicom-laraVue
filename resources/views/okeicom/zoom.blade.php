<div id="container">
  <div class="midashi01">Zoomとは？</div>
  <div class="center"><img src="/img/okeicom/zoom01.jpg" alt="メイン画像"></div>
  <p class="explain">Zoomは、お手持ちのパソコン・スマートフォン・タブレットなどを使用して、遠くにいる人とも気軽にオンラインでビデオ通話ができるツールです。</p>
  <p class="explain">他のビデオ通話ツールと比較して、参加者のアカウント作成が不要であり、接続も安定しているので、複数人でのオンラインレッスンでも快適に利用することができます。 </p>
  <p class="explain">ただし、無料版では3人以上で接続する場合には40分の時間制限があります。40分以上のレッスンを行う講師の方は有料版の利用をおすすめします。</p>
  <div class="midashi01">Zoomのインストール方法</div>
  <p class="explain">Zoomを利用するには、使用するパソコン・スマートフォン・タブレットにアプリをインストールします。</p>
  <p class="explain">Zoomのインストールは、講師の方も受講する方も必ず行ってください。<br>
    &nbsp;</p>
  <div class="fee">
    <div class="midashi05">パソコンへのインストール方法</div>
    <div class="midashi03">STEP１</div>
    <p>Zoomのアプリは、公式ホームページからダウンロードすることができます。<a href="https://zoom.us/jp-jp/meetings.html" target="_blank">公式ホームページはこちら</a>から。</p>
    <div class="center"><img src="/img/okeicom/install01.jpg" width="700" alt="インストール１"></div>
    <br>
    <div class="midashi03">STEP2</div>
    <p>公式ホームページ下部の「ダウンロード」の中の「ミーティングクライアント」をクリックします。</p>
    <div class="center"><img src="/img/okeicom/install02.jpg" width="700" alt="インストール２"></div>
    <br>
    <div class="midashi03">STEP3</div>
    <p>ダウンロードセンターのページへ移ったら、一番上の「ミーティング用Zoomクライアント」のダウンロードをクリックします。ダウンロードが完了するまで待ちましょう。</p>
    <div class="center"><img src="/img/okeicom/install03.jpg" width="700" alt="インストール３"></div>
    <br>
    <div class="midashi03">STEP4</div>
    <p>ダウンロードが完了すると、パソコン内のダウンロードフォルダにZoomのアイコンが表示されます。そのアイコンをダブルクリックします。</p>
    <div class="center"><img src="/img/okeicom/install06.jpg" width="700" alt="インストール４"></div>
    <br>
    <div class="midashi03">STEP5</div>
    <p>インストーラーが起動して、パソコンにZoomアプリがインストールされます。完了するまでしばらくお待ちください。</p>
    <div class="center"><img src="/img/okeicom/install04.jpg" width="700" alt="インストール５"></div>
    <br>
    <div class="midashi03">STEP6</div>
    <p>インストールが完了すると、自動的に『Zoomクラウドミーティング』が立ち上がります。<br>
      受講者の方は右上の「×」で閉じて問題ありません。講師の方はサインインをクリックして、アカウント作成を行います。</p>
    <div class="center"><img src="/img/okeicom/install05.jpg" width="700" alt="インストール６"></div>
    <br>
  </div>
  <div class="fee">
    <div class="midashi05">スマートフォン、タブレットへのインストール方法</div>
    <div class="midashi03">STEP１</div>
    Zoomのアプリは、App StoreまたはGoogle Playからインストールを行ってください。<br>
    &nbsp;<br>
    <div class="center"><a href="https://apps.apple.com/us/app/zoom-cloud-meetings/id546505307?itsct=apps_box_badge&amp;itscg=30200"><img src="/img/okeicom/apple.png" width="300" alt="メイン画像"></a>&nbsp;<a href="https://play.google.com/store/apps/details?id=us.zoom.videomeetings&hl=ja&pcampaignid=pcampaignidMKT-Other-global-all-co-prtnr-py-PartBadge-Mar2515-1"><img src="/img/okeicom/google.png" width="300" alt="メイン画像"></a></div>
    <br>
    <div class="center"><img src="/img/okeicom/install07.jpg" width="500" alt="インストール７"></div>
  </div>
  <div class="midashi01">アカウント開設方法(講師向け)</div>
  <p class="explain">レッスンを開催する講師の方は、Zoomアプリをインストールしたら、サインイン（アカウント作成）を行います。<br>
    オンラインレッスンに参加するのみの受講者の方のサインインは不要です。<br>
    &nbsp;</p>
  <div class="fee">
    <div class="midashi03">STEP１</div>
    <p>講師の方はZoomへのサインインが必要となります。『Google』や『Facebook』のアカウントでサインインすることもできますが、ここでは「無料でサインアップ」からZoomアカウントを作成します。</p>
    <div class="center"><img src="/img/okeicom/signup01.jpg" width="500" alt="サインイン１"></div>
    <br>
    <div class="midashi03">STEP2</div>
    <p>生年月日を入力します。Zoomは16歳以下の登録ができないので、その確認です。</p>
    <div class="center"><img src="/img/okeicom/signup02.jpg" width="700" alt="サインイン２"></div>
    <br>
    <div class="midashi03">STEP3</div>
    <p>メールアドレスを入れて、サインアップをクリックします。</p>
    <div class="center"><img src="/img/okeicom/signup03.jpg" width="700" alt="サインイン３"></div>
    <br>
    <div class="midashi03">STEP4</div>
    <p>入力したメールアドレス宛にZoomからメールが送られます。</p>
    <div class="center"><img src="/img/okeicom/signup04.jpg" width="700" alt="サインイン４"></div>
    <br>
    <div class="midashi03">STEP5</div>
    <p>届いたメールを確認し、メール本文内の「アカウントをアクティベート」をクリックします。</p>
    <div class="center"><img src="/img/okeicom/signup05.jpg" width="500" alt="サインイン５"></div>
    <br>
    <div class="midashi03">STEP6</div>
    <p>名前、苗字、パスワード、パスワード確認を入力しします。パスワードは大文字・小文字・数字を含む必要があります。<br>
      「私はロボットではありません」にチェックをし、「続ける」をクリックします。</p>
    <div class="center"><img src="/img/okeicom/signup06.jpg" width="700" alt="サインイン６"></div>
    <br>
    <div class="midashi03">STEP7</div>
    <p>こちらのページは「手順をスキップする」で次へ進みます。</p>
    <div class="center"><img src="/img/okeicom/signup07.jpg" width="700" alt="サインイン７"></div>
    <br>
    <div class="midashi03">STEP8</div>
    <p>こちらのページに切り替わったら、アカウントの作成は完了です。ミーティングの開催については次の項目をご覧ください。</p>
    <div class="center"><img src="/img/okeicom/signup08.jpg" width="700" alt="サインイン８"></div>
    <br>
  </div>
  <div class="midashi01">ミーティング開催方法(講師向け)</div>
  <p class="explain">おけいcomにてライブレッスンを行うには、Zoomのミーティングアドレスを入力する必要があります。</p>
  <p class="explain">ミーティングアドレスは、Zoomアプリからミーティング予定を設定すると取得することができます。<br>
    &nbsp;</p>
  <div class="fee">
    <div class="midashi03">STEP１</div>
    <p>Zoomのアプリを起動させ、メールアドレスとパスワードを入力してサインインします。</p>
    <div class="center"><img src="/img/okeicom/meeting01.jpg" width="500" alt="ミーティング１"></div>
    <br>
    <div class="midashi03">STEP2</div>
    <p>「スケジュール」を選択して、ミーティングの設定を行います。</p>
    。
    <div class="center"><img src="/img/okeicom/meeting02.jpg" width="700" alt="ミーティング２"></div>
    <br>
    <div class="midashi03">STEP3</div>
    <p>設定画面が開くので、各項目を入力または選択していきます。<br>
      ①トピック：ミーティングのタイトル。<br>
      ②開始日時：年月日と時間を設定。時間は15分単位です。<br>
      ③持続時間：レッスンの所要時間を設定。設定時間を過ぎても自動で終了はしませんが、無料プランは40分以上の開催はできません。<br>
      ④ミーティングID：「自動的に生成」のままの設定で良いです。<br>
      ⑤待機室：ミーティングへの参加許可をホスト自身がコントロールできます。<br>
      ⑥ビデオ：レッスン時に映像が必要な場合はオンにします。（基本的に講師側はオンがおすすめ）<br>
      ⑦カレンダー：特に外部カレンダーと連動させる必要はないので「他のカレンダー」を選択します。 </p>
    <div class="center"><img src="/img/okeicom/meeting03.jpg" width="500" alt="ミーティング３"></div>
    <br>
    <div class="midashi03">STEP4</div>
    <p>詳細オプションの右横をクリックすると、詳細の設定ができます。必要に応じてチェックを入れましょう。</p>
    <div class="center"><img src="/img/okeicom/meeting04.jpg" width="500" alt="ミーティング４"></div>
    <br>
    <div class="midashi03">STEP5</div>
    <p>設定を保存するとミーティング情報が表示されます。「https://~」で始まるアドレスが、おけいcomのレッスン作成時に入力するミーティングアドレスです。</p>
    <div class="center"><img src="/img/okeicom/meeting05.jpg" width="500" alt="ミーティング５"><br>
      <img src="/img/okeicom/meeting10.jpg" width="500" alt="ミーティング６"> </div>
    <br>
    <div class="midashi03">STEP6</div>
    <p>設定したミーティングはアプリからいつでも確認できます。<br>
      実際にレッスンを開始する際、５分前には「開始」を押して待機しましょう。</p>
    <div class="center"><img src="/img/okeicom/meeting06.jpg" width="700" alt="ミーティング７"></div>
    <br>
    <div class="midashi03">STEP7</div>
    <p>「開始」を押すと、ミーティングが立ち上がります。受講者が参加すると、待機室入室のお知らせが表示されます。<br>
      レッスン購入済みの受講者であると確認できたら、「許可する」を選択してください。</p>
    <div class="center"><img src="/img/okeicom/meeting07.jpg" width="700" alt="ミーティング８"></div>
    <br>
    <div class="midashi03">STEP8</div>
    <p>受講者が入室すると画面上に受講者が表示されます。ビデオの切替、チャットでのメッセージ送信、画面の共有など必要に応じて活用してください。</p>
    <div class="center"><img src="/img/okeicom/meeting08.jpg" width="700" alt="ミーティング９"></div>
    <br>
    <div class="midashi03">STEP9</div>
    <p>レッスンが終了したら、右下の「終了」→「全員に対してミーティングを終了」を選択して接続を終了してください。</p>
    <div class="center"><img src="/img/okeicom/meeting09.jpg" width="700" alt="ミーティング１０"></div>
    <br>
  </div>
